/**
 * AMath Hardened Dual-Boundary Runtime Architecture v2.0
 * 
 * Core Principle:
 *   runtime proves software behavior
 *   evidence proves external reality
 * 
 * This model creates strict separation between:
 *   - deterministic runtime observations
 *   - external claims requiring corroboration
 *   - interpretation layers
 *   - visualization surfaces
 */

// ============================================================
// CORROBORATION MODEL
// ============================================================

/**
 * Hardened Corroboration Status
 * 
 * | Status                | Meaning                        |
 * |-----------------------|--------------------------------|
 * | RUNTIME_VERIFIED      | Proven by code execution       |
 * | CORROBORATED          | Supported by external evidence |
 * | PENDING_CORROBORATION | Awaiting supporting material   |
 * | DISPUTED              | Evidence conflict exists       |
 * | UNVERIFIED            | No validation available        |
 */
export type CorroborationStatus =
  | 'RUNTIME_VERIFIED'
  | 'CORROBORATED'
  | 'PENDING_CORROBORATION'
  | 'DISPUTED'
  | 'UNVERIFIED';

// ============================================================
// RUNTIME PROVENANCE
// ============================================================

/**
 * Runtime Provenance Layer
 * 
 * Every runtime-confirmed result carries provenance.
 * Provenance prevents ambiguity.
 * 
 * Without provenance: "this happened"
 * With provenance: "this happened because runtime XYZ produced it under build ABC"
 */
export interface RuntimeProvenance {
  /** Build hash or commit SHA */
  buildHash: string;
  /** ISO timestamp of generation */
  generatedAt: string;
  /** Verification source */
  verifiedBy: 'build' | 'api' | 'runtime';
  /** Deployment environment */
  environment: 'dev' | 'preview' | 'production';
  /** Source route or module */
  routeSource: string;
  /** Optional integrity checksum */
  checksum?: string;
}

// ============================================================
// SOURCE CLASSIFICATION
// ============================================================

/**
 * Source Type Classification
 * 
 * - runtime: Generated by code execution
 * - external: From third-party systems
 * - manual: Human-entered data
 */
export type SourceType = 'runtime' | 'external' | 'manual';

// ============================================================
// EVIDENCE ROW MODEL
// ============================================================

/**
 * Hardened Evidence Row
 * 
 * All high-value tables include:
 *   sourceType
 *   corroboration
 *   lastReviewedAt
 *   provenance
 */
export interface EvidenceRow {
  id: string;
  label: string;
  value: string;
  sourceType: SourceType;
  corroboration: CorroborationStatus;
  lastReviewedAt: string;
  provenance?: RuntimeProvenance;
}

// ============================================================
// RECEIPT V2 MODEL
// ============================================================

/**
 * Hardened Receipt Layer
 * 
 * Receipt objects distinguish runtime proof from narrative context.
 */
export interface ReceiptV2 {
  receiptId: string;
  route: string;
  decision: string;
  generatedAt: string;

  /** Whether this receipt was generated by runtime execution */
  runtimeVerified: boolean;
  /** Corroboration status of the underlying claim */
  corroboration: CorroborationStatus;

  /** Full provenance chain */
  provenance: RuntimeProvenance;
}

// ============================================================
// RUNTIME CLASSIFICATION MODEL
// ============================================================

/**
 * AMath Runtime Classification
 * 
 * Runtime Layer → Policy Layer → Evidence Layer → Presentation Layer
 * 
 * Meaning:
 *   presentation never decides truth
 *   policy never invents evidence
 *   runtime never claims external certainty
 */
export type RuntimeLayer = 'runtime' | 'policy' | 'evidence' | 'presentation' | 'corroboration';

/**
 * Layer Responsibility Matrix
 */
export interface LayerResponsibility {
  layer: RuntimeLayer;
  responsibility: string;
  canDecideTruth: boolean;
  canInventEvidence: boolean;
  canClaimExternalCertainty: boolean;
}

