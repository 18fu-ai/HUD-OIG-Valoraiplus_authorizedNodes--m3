// ============================================================
// OBSERVED VS INTERPRETATION
// ============================================================

/**
 * Observed Runtime Facts
 * 
 * Examples:
 *   18 API endpoints responded successfully
 *   Build completed without export errors
 *   Receipt pipeline executed
 *   Topology engine returned route71
 */
export interface ObservedFact {
  id: string;
  observation: string;
  timestamp: string;
  source: 'build' | 'api' | 'runtime' | 'topology';
  metric?: number;
}

/**
 * Interpretation Layer
 * 
 * Examples:
 *   Operationally stable
 *   Ready for deployment
 *   Awaiting external corroboration
 * 
 * This prevents runtime logs from becoming narrative assertions.
 */
export interface Interpretation {
  id: string;
  statement: string;
  basedOn: string[]; // IDs of ObservedFacts
  confidence: 'high' | 'medium' | 'low';
  requiresCorroboration: boolean;
}

// ============================================================
// HARDENED CLOSED LOOP
// ============================================================

/**
 * Hardened Closed Loop Stages
 * 
 * Input → Validation → Policy → Decision → Receipt → Provenance → Route → Visualization → Corroboration Layer
 * 
 * This guarantees deterministic lifecycle tracking.
 */
export type ClosedLoopStage =
  | 'input'
  | 'validation'
  | 'policy'
  | 'decision'
  | 'receipt'
  | 'provenance'
  | 'route'
  | 'visualization'
  | 'corroboration';

export interface ClosedLoopState {
  currentStage: ClosedLoopStage;
  completedStages: ClosedLoopStage[];
  timestamp: string;
  traceId: string;
}

